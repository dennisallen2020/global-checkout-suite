export const getTranslation = (language: string, key: string): string => {
  return translations[language]?.[key] || translations['en'][key] || key;
};

export const formatCurrency = (amount: number, currency: string, language: string): string => {
  const symbol = currencySymbols[currency] || currency;
  const formatter = new Intl.NumberFormat(language === 'pt' ? 'pt-BR' : language, {
    style: 'currency',
    currency: currency,
    minimumFractionDigits: currency === 'JPY' ? 0 : 2
  });
  
  try {
    return formatter.format(amount);
  } catch {
    return `${symbol} ${amount.toFixed(currency === 'JPY' ? 0 : 2)}`;
  }
};

import { currencySymbols } from '../config/checkout-config';
